// Number
let age = 21;
console.log(age);
console.log(typeof age);

// string
let number = "21";
console.log(number);
console.log(typeof number);

// boolean
let isStudent = true;
console.log(typeof isStudent);

// Null
let car = null;
console.log(typeof car);

// symbol
let mySymbol = Symbol();
console.log(typeof mySymbol);
